					<h2>Paris</h2>

				    <p>Découvrez la capitale française à travers son histoire et son actualité artistiques, économiques et culturelles.
Rejoignez-nous dans des circuits qui offrent une vision riche et diversifiée de la Ville Lumière. Découvrez comment et pourquoi Paris est Paris, capitale artistique et intellectuelle qui a exercé une influence majeure à travers le monde au cours des XVIIème, XIXème siècles et une bonne partie du XXe siècle, et qui continue encore aujourd'hui de nous fasciner par son rayonnement intellectuel, historique, son charme esthétique et sa vitalité.

</p>

				    <p>Capitale de la gastronomie, de la mode et du glamour, mais aussi berceau et épicentre des mouvements politiques qui ont créé le monde moderne et les tendances artistiques internationales d'avant-garde comme l'impressionnisme, le cubisme, le fauvisme, le surréalisme et bien d'autres. Lieu de naissance de la photographie, du cinéma, de l'urbanisme moderne avec Haussmann. Elle attire aussi les écrivains qui en font la capitale mondiale des lettres. Elle est en effet la destination privilégiée de la « génération perdue» américaine ainsi que des auteurs du « Boom Latino-Américain » dont les principaux représentants s'affirment comme écrivains dans la capitale française.</p>

				    <p>Ainsi, Paris offre un cadre idéal et une multitude d'angles pour la découvrir ou la redécouvrir. En plus des visites incontournables des principaux musées et points d'intérêts, nous souhaitons vous proposer plusieurs thèmes qui illustrent l'importance que la ville a eu dans l'histoire de la France, de l'Europe et du monde.</p>
