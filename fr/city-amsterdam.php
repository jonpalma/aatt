					<h2>Ámsterdam</h2>
					<p>  Venise du nord, ville des péniches et des terres récupérées sur la mer du Nord. Son anneau de canaux, emblème du siècle d'or hollandais, récemment inscrit au patrimoine mondial de l'UNESCO, est l'un des centres historiques européens les plus beaux et les mieux conservés. Célèbre pour ses bicyclettes, ses fromages et ses tulipes, mais aussi pour sa longue tradition de tolérance et d'ouverture, Amsterdam est un précurseur du capitalisme avec la plus ancienne bourse du monde et la célèbre Compagnie des Indes Orientales, l'une des premières sociétés transnationales. Ville portuaire, leader à l'ère des grandes explorations, centre du commerce et de la finance, Amsterdam a réussi à se moderniser sans détruire son patrimoine historique, créant une multitude d’ensembles architecturaux emblématiques. </p>
