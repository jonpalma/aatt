					<h2>Ámsterdam</h2>
					<p>Venecia del norte, ciudad con casas flotantes y tierras reclamadas al mar del norte.  Su anillo de canales, emblema del siglo de oro holandés, recientemente inscrito en el patrimonio mundial de la UNESCO, constituye uno de los centros históricos europeos más bellos y mejor conservados.</p>
					<p>Famosa por sus bicicletas, quesos y tulipanes, pero también por su larga tradición de tolerancia y apertura, Ámsterdam fue precursor del capitalismo contando con  la bolsa de valores más antigua del mundo y la compañía de indias orientales, una de las primeras empresas transnacionales. Ciudad portera, líder en la época de grandes exploraciones, centro de comercio  y  de finanzas, Ámsterdam ha logrado modernizarse sin destruir su patrimonio histórico,  creando una multitud de complejos arquitectónicos emblemáticos. </p>
